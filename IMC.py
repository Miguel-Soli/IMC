print("--------------------------------------------------------------------------------")
print("CALCULO DE IMC")
print("--------------------------------------------------------------------------------")
nome =(input("Digite o seu nome: "))
print("--------------------------------------------------------------------------------")
peso =float(input("Digite seu peso: "))
print("--------------------------------------------------------------------------------")
altura =float(input("Digite sua altura: "))
print("--------------------------------------------------------------------------------")
#Conta do IMC
Imc= peso/(altura*altura)
#mensagens
if Imc >= 18.6:
    print("Peso normal😁😁😁")
    print("--------------------------------------------------------------------------------")
elif Imc >= 25:
   print("Sobrepeso!😞😞😞")
   print("--------------------------------------------------------------------------------")
else:
  print("Baixo peso!!💀💀💀")
  print("--------------------------------------------------------------------------------")

print(f"{nome},Seu IMC é {Imc:.4}")
print("--------------------------------------------------------------------------------")